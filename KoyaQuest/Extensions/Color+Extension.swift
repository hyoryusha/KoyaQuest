import SwiftUI

extension Color {
    static let koyaOrange = Color("Orange")
    static let koyaRed = Color("FailColor")
    static let koyaGreen = Color("SuccessColor")
    static let koyaPurple = Color("Purple")
    static let koyaSky = Color("LightBlue")
    static let koyaDarkText = Color("DarkTextColor")
    static let koyaLightText = Color("LightTextColor")
}

extension UIColor {
    static let koyaOrange = UIColor(named: "Orange")
    static let koyaRed = UIColor(named: "FailColor")
    static let koyaGreen = UIColor(named: "Success")
    static let koyaPurple = UIColor(named: "Purple")
    static let koyaSky = UIColor(named: "LightBlue")
    static let koyaDarkText = UIColor(named: "DarkTextColor")
    static let koyaLightText = UIColor(named: "LightTextColor")
}
